st.title("🌐 Skala Atmosfer Global")

st.markdown("""
Fenomena skala global adalah dinamika atmosfer yang mencakup wilayah sangat luas, hingga mencakup seluruh belahan bumi. Dampaknya sangat signifikan terhadap pola cuaca dan iklim dunia.

Berikut beberapa fenomena utama:

- **ENSO (El Niño - La Niña)**: Perubahan suhu permukaan laut di Samudra Pasifik tropis. 
  - El Niño cenderung menyebabkan musim kemarau di Indonesia.
  - La Niña meningkatkan curah hujan dan potensi bencana hidrometeorologis.

- **IOD (Indian Ocean Dipole)**: Anomali suhu di Samudra Hindia.
  - IOD Positif → kering di Indonesia barat.
  - IOD Negatif → basah dan hujan meningkat.

- **MJO (Madden-Julian Oscillation)**: Gangguan konvektif bergerak dari barat ke timur.
  - MJO aktif di fase tertentu dapat memicu hujan deras di Indonesia.

""")

